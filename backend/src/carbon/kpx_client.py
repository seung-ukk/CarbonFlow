# src/carbon/kpx_client.py
import requests
from datetime import datetime
from src.core.config import settings

class KPXClient:
    def __init__(self):
        # [보안 세팅] 공공데이터포털에서 발급받은 '디코딩 인증키'를 .env -> config를 거쳐 안전하게 로드
        self.service_key = settings.WEATHER_API_KEY
        
        # [기상청 API 엔드포인트 선택: 초단기실황조회 (getUltraSrtNcst)]
        # 가장 최근 '이 시간'에 실제 관측된 날씨(실황) 데이터를 실시간으로 가져오는 핵심 API 주소입니다.
        self.url = "http://apis.data.go.kr/1360000/VilageFcstInfoService_2.0/getUltraSrtNcst"

    def get_realtime_intensity(self) -> float:
        """
        기상청 외부 서버와 실시간 HTTP 통신을 수행하여 현재 날씨를 파싱하고,
        해커톤 기획 아이템의 핵심 알고리즘인 [태양광 발전 효율 기반 실시간 탄소강도(gCO2/kWh)]를 계산하여 반환합니다.
        """
        
        # =========================================================================
        # 논리 1. [기상청 타임라인 동기화 알고리즘]
        # 기상청 초단기실황 API는 매시 '40분'에 실제 최신 데이터가 생성되어 시스템에 반영됩니다.
        # 만약 현재 시각이 14시 15분이라면, 14시 데이터는 아직 기상청 서버에 존재하지 않습니다.
        # 따라서 현재 정각(0분) ~ 39분 사이일 때는 안전하게 '이전 시간(13시)' 데이터를 요청해야 에러가 나지 않습니다.
        # =========================================================================
        now = datetime.now()
        base_date = now.strftime("%Y%m%d") # API 규격에 맞게 연월일 포맷팅 (예: '20260522')
        
        if now.minute < 40:
            # 현재 40분이 안 되었으므로, 기상청 서버 기준 아직 생성되지 않은 시간대입니다. -> 1시간 전 데이터 세팅
            if now.hour == 0:
                # 만약 현재 시각이 새벽 00시 20분이라면, 하루 전날인 '어제 23시' 데이터를 요청해야 하는 예외를 처리합니다.
                from datetime import timedelta
                yesterday = now - timedelta(days=1)
                base_date = yesterday.strftime("%Y%m%d")
                base_time = "2300"
            else:
                # 일반적인 경우 1시간을 빼서 2자리 숫자로 포맷팅 (예: 14시 15분 -> '1300')
                base_time = f"{now.hour - 1:02d}00"
        else:
            # 현재 40분이 넘었으므로, 기상청 서버에 방금 생성된 따끈따끈한 현재 정시 데이터를 요청합니다.
            base_time = f"{now.hour:02d}00"

        # =========================================================================
        # 논리 2. [기상청 전용 격자 좌표 (nx, ny) 매핑]
        # 기상청은 전 세계 표준 위도/경도(예: 37.123, 127.123) 시스템을 사용하지 않고, 
        # 대한민국 영토를 가로세로 바둑판 형태로 쪼갠 자체 '격자 좌표계'를 사용합니다.
        # - nx: 행망(X축 고유 번호) / ny: 열망(Y축 고유 번호)
        # - nx=61, ny=127 주소는 삼육대학교 및 인근 행정구역(서울 성북구/노원구 일대)을 정확히 타겟팅하는 좌표입니다.
        # =========================================================================
        params = {
            "serviceKey": self.service_key, # 공공데이터포털 인증키
            "pageNo": "1",                  # 페이지 번호 (기본값 1)
            "numOfRows": "100",             # 한 페이지에 불러올 데이터 개수 (기본 항목 전체 수집을 위해 100 세팅)
            "dataType": "JSON",             # 기본 응답은 XML이므로, 파이썬이 요리하기 편한 JSON 형식으로 강제 지정
            "base_date": base_date,         # 위 논리 1에서 보정된 연월일
            "base_time": base_time,         # 위 논리 1에서 보정된 시간대
            "nx": "61",                     # 서울 성북/노원 기준 기상청 X 격자 좌표
            "ny": "127"                     # 서울 성북/노원 기준 기상청 Y 격자 좌표
        }

        # [안전장치 백업 데이터 설정] 
        # 기상청 서버가 터지거나 네트워크가 단절될 경우 백엔드 전체가 다운되는 것을 막기 위한 대한민국 평균 기저부하 탄소 수치
        base_carbon_intensity = 420.0

        try:
            # 3. requests 라이브러리를 사용해 정부 공공데이터 서버로 실제 HTTP GET 요청을 전송합니다.
            # 타임아웃을 5초로 제한하여, 공공 서버의 응답이 지연되더라도 우리 백엔드가 무한 대기 상태에 빠지는 것을 차단합니다.
            response = requests.get(self.url, params=params, timeout=5)
            
            # 네트워크 통신 자체가 실패(예: 404, 503 에러 등)했는지 검증
            if response.status_code != 200:
                print(f"[Warning] 기상청 API 연결 실패 (HTTP {response.status_code}). 백업 기본값(420.0)을 적용합니다.")
                return base_carbon_intensity

            # 응답받은 Raw 텍스트 데이터를 파이썬 딕셔너리 객체로 변환
            data = response.json()
            
            # =========================================================================
            # 논리 3. [공공데이터포털 특유의 이중 응답 구조 예외 처리]
            # 공공데이터 API는 주소가 맞거나 키가 틀려도 일단 무조건 HTTP 200 OK를 뱉는 고질적인 버그가 있습니다.
            # 진짜 성공했는지는 응답 내용 내부의 'resultCode'가 '00'(정상 완료)인지 반드시 체크해야 합니다.
            # 만약 키가 틀렸다면 여기서 'SERVICE_KEY_IS_NOT_REGISTERED_ERROR' 등을 걸러냅니다.
            # =========================================================================
            header = data.get("response", {}).get("header", {})
            if header.get("resultCode") != "00":
                print(f"[Warning] 기상청 시스템 에러 발각: {header.get('resultMsg')}. 백업 기본값을 적용합니다.")
                return base_carbon_intensity

            # 실제 기상 관측 데이터 리스트(배열) 추출 성공
            items = data["response"]["body"]["items"]["item"]
            
            # [초기 상태 정의] 아무런 비나 눈 소식이 없으면 기본적으로 맑은 하늘(CLEAR) 상태로 인지 시작
            weather_status = "CLEAR" 
            
            # =========================================================================
            # 논리 4. [기상청 관측 카테고리 기호(category) 해석 및 파싱]
            # 기상청은 데이터 이름 대신 영문 3글자의 약어 기호를 내려줍니다. 팀원들이 볼 주요 기호 사전:
            # - T1H : 1시간 기온 (℃)
            # - REH : 습도 (%)
            # - RN1 : 1시간 강수량 (mm) -> 태양광 효율 감점 요인
            # - PTY : 강수형태 (가장 중요 💡) -> 0(없음), 1(비), 2(비/눈), 3(눈), 4(소나기)
            # =========================================================================
            for item in items:
                # 기상청 항목 중 '강수형태(PTY)' 데이터를 찾아냅니다.
                if item.get("category") == "PTY":
                    # obsrValue는 실제 기상청 관측값이며 데이터 타입이 문자열이므로 정수형(int)으로 변환합니다.
                    pty_value = int(item.get("obsrValue", 0))
                    
                    # 관측값이 1(비), 2(비/눈), 4(소나기)인 경우 현재 날씨 상태를 우천(RAINY)으로 확정합니다.
                    if pty_value in [1, 2, 4]:
                        weather_status = "RAINY"
                    # 관측값이 3(눈)인 경우 기상 상태를 적설(SNOWY)로 확정합니다.
                    elif pty_value == 3:
                        weather_status = "SNOWY"

            # =========================================================================
            # 논리 5. [해커톤 아이템 핵심 비즈니스 로직 - 탄소강도 연산 알고리즘]
            # 우리 서비스의 핵심 기믹은 "실시간 기상 상태에 연동되는 친환경 전력망 모방"입니다.
            # 태양광 발전이 폭발적으로 일어나는 피크 타임인 낮 시간대(09시 ~ 16시)의 날씨를 감지합니다.
            # =========================================================================
            if 9 <= now.hour <= 16:
                if weather_status == "CLEAR":
                    # 비/눈 소식 없이 맑다면 전력망에 태양광 신재생 친환경 에너지 공급량이 급증한 시나리오를 발동합니다.
                    # 화력 발전소 가동률이 줄어들므로, 실시간 탄소 배출 지수(탄소강도)를 대폭 깎아서 반환합니다.
                    print(f"[{now.strftime('%H:%M')}] 기상청 실황 데이터 분석: 맑음 ☀️ (태양광 버프 반영 -> 탄소강도 320.5로 급감)")
                    return 320.5
                else:
                    # 낮 시간대임에도 비나 눈이 와서 태양광 패널이 차단되면 친환경 공급량이 급감합니다.
                    # 전력 부족을 메우기 위해 LNG/석탄 화력 발전 비중이 늘어나므로 탄소 배출 지수를 높게 책정합니다.
                    print(f"[{now.strftime('%H:%M')}] 기상청 실황 데이터 분석: 비/눈/흐림 🌧️ (태양광 발전 차단 -> 탄소강도 455.2로 급증)")
                    return 455.2
            
            # 태양광 발전이 불가능한 야간, 새벽 시간대이거나 기저부하만 도는 일반 시간대 기본 탄소 지수
            return 390.8

        except Exception as e:
            # 주소 오타, JSON 키 파싱 실패 등 코드 자체에서 예기치 못한 크리티컬 에러 발생 시
            # 시스템 전체가 뻗지 않고 유연하게 대처할 수 있도록 안전 트랩(Catch)을 구성합니다.
            print(f"[Critical Error] 외부 기상청 API 파싱 과정 중 예외 발생: {e}. 시스템 안전을 위해 기본값 420.0을 보냅니다.")
            return base_carbon_intensity